package org.springframework.data.neo4j.integration.conversion.domain;

import java.lang.annotation.ElementType;

import org.neo4j.ogm.annotation.GeneratedValue;
import org.neo4j.ogm.annotation.Id;
import org.neo4j.ogm.annotation.NodeEntity;
import org.neo4j.ogm.annotation.typeconversion.Convert;

/**
 * Models an element of the Java programming language, although more importantly contains an enum that isn't part of the
 * scanned OGM meta-data and therefore tests an important part of the Spring conversion service.
 */
@NodeEntity
public class JavaElement {

	@Id @GeneratedValue private Long identifier;
	private String name;
	@Convert(graphPropertyType = String.class) private ElementType elementType;

	public Long getIdentifier() {
		return identifier;
	}

	public void setIdentifier(Long identifier) {
		this.identifier = identifier;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public ElementType getElementType() {
		return elementType;
	}

	public void setElementType(ElementType elementType) {
		this.elementType = elementType;
	}

}
